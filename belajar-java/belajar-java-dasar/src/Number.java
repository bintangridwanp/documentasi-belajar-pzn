public class Number {

    public static void main(TipeDataString[] args) {

        // Deklarasi variabel tipe data byte, dengan nilai maksimum 127
        byte iniByte = 127;

        // Deklarasi variabel tipe data short, dengan nilai maksimum 32767
        short iniShort = 32767;

        // Deklarasi variabel tipe data int, dengan nilai maksimum 2147483647
        int iniInt = 2147483647;

        // Deklarasi variabel tipe data long, dengan nilai maksimum 9223372036854775807
        long iniLong = 9223372036854775807L;

        // Deklarasi variabel tipe data float, dengan nilai 123.456
        float iniFloat = 123.456f;

        // Deklarasi variabel tipe data double, dengan nilai 123.456
        double iniDouble = 123.456d;

        // Deklarasi variabel tipe data int untuk nilai desimal biasa
        int decimalInt = 12345;

        // Deklarasi variabel tipe data int untuk nilai hexadecimal (0xFFF = 4095 dalam desimal)
        int hexaDecimalInt = 0xFFF;

        // Deklarasi variabel tipe data int untuk nilai biner (0b1010 = 10 dalam desimal)
        int binaryInt = 0b1010;

        // Deklarasi variabel tipe data long dengan nilai 1.000.000 (underscore untuk kejelasan)
        long amount = 1_000_000;
    }

}
