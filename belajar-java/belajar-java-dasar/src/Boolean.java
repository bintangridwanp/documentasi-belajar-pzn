public class Boolean {
    public static void main(TipeDataString[] args) {

        // Deklarasi variabel tipe data boolean dengan nilai true (benar)
        boolean benar = true;

        // Deklarasi variabel tipe data boolean dengan nilai false (salah)
        boolean salah = false;

        // Mencetak nilai variabel boolean 'benar' (true)
        System.out.println(benar);

        // Mencetak nilai variabel boolean 'salah' (false)
        System.out.println(salah);

    }
}
