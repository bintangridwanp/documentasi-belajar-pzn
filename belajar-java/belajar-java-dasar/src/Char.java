public class Char {
    public static void main(TipeDataString[] args) {

        // Deklarasi variabel tipe data char untuk menyimpan karakter tunggal
        char a = 'A';
        char b = 'B';
        char c = 'C';

        // Mencetak karakter a, b, dan c masing-masing pada baris baru
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        // Mencetak karakter a, b, dan c pada baris yang sama tanpa menambahkan baris baru
        System.out.print(a);
        System.out.print(b);
        System.out.print(c);

    }
}
