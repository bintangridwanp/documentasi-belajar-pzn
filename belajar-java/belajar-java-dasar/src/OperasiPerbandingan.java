public class OperasiPerbandingan {
    public static void main(String[] args) {

        var a = 100; // Deklarasi variabel 'a' dengan nilai 100
        var b = 200; // Deklarasi variabel 'b' dengan nilai 200

        // Operasi perbandingan lebih besar
        System.out.println(a > b); // Menampilkan hasil perbandingan a > b (100 > 200)

        // Operasi perbandingan lebih kecil
        System.out.println(a < b); // Menampilkan hasil perbandingan a < b (100 < 200)

        // Operasi perbandingan lebih kecil atau sama dengan
        System.out.println(a <= b); // Menampilkan hasil perbandingan a <= b (100 <= 200)

        // Operasi perbandingan lebih besar atau sama dengan
        System.out.println(b >= a); // Menampilkan hasil perbandingan b >= a (200 >= 100)

        // Operasi perbandingan sama dengan
        System.out.println(a == b); // Menampilkan hasil perbandingan a == b (100 == 200)

        // Operasi perbandingan tidak sama dengan
        System.out.println(a != b); // Menampilkan hasil perbandingan a != b (100 != 200)

    }
}
